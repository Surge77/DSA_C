/*

Queue ADT -> FIFO - First in first out 

Insertion from tail and deletion from the head

Definition: A list or collection with the restriction that insertion can be performed at one end (rear) and deletion can be performed at other end(front)

Operations:
1.Enqueue() or Push()
2.Dequeue() or pop()
3.front() or Peek() -> shows element at the front or head
4.IsEmpty()

all these operations must take constant time i.e O(1) 

Applications:

1.Network printer
2.Used mostly where there are shared resources and only one request can be handled at a time
*/